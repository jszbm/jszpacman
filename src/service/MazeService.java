    public int[][] generateMaze(int rows, int columns) {

        int[][] maze = new int[rows][columns];

        for (int i = 0; i < maze.length; i++){

            if(i == 0 || i == rows - 1){
                Arrays.fill(maze[i], 1);
            }

            maze[i][0] = 1;
            maze[i][columns - 1] = 1;
        }


        maze[4][4] = 6;

        maze [2][2] = 3;

        System.out.println("generated");

        return maze;
    }


